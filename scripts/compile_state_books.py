def parse_args() -> argparse.Namespace:
    _help_workers = f"Max concurrent xelatex processes (default: {MAX_WORKERS}, override with MAX_WORKERS in .env)"
    _help_passes = f"Number of xelatex passes per book (default: {XELATEX_PASSES}, override with XELATEX_PASSES in .env)"

    parser = argparse.ArgumentParser(
        description="Compile state-specific Grade 4 math LaTeX books and collect PDFs.",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=(
            "examples:\n"
            "  compile_state_books.py --book-type all_in_one                     # all 50 states\n"
            "  compile_state_books.py --book-type study_guide                    # all 50 states (brief)\n"
            "  compile_state_books.py --book-type all --states texas             # all book types, one state (concurrent!)\n"
            "  compile_state_books.py --book-type all_in_one --states texas,florida\n"
            "  compile_state_books.py --book-type study_guide --jobs 8\n"
            "  compile_state_books.py --book-type all_in_one --passes 3\n"
        ),
    )
    parser.add_argument(
        "--workspace",
        type=Path,
        default=None,
        help="Workspace root directory (auto-detected if omitted)",
    )
    _all_choices = list(ALL_BOOK_TYPES.keys()) + ["all", "all_previews"]
    parser.add_argument(
        "--book-type",
        choices=_all_choices,
        default=None,
        help="Book type to compile. Use 'all' for every non-preview type, 'all_previews' for all preview variants. (interactive if omitted)",
    )
    parser.add_argument(
        "--states",
        type=str,
        default=None,
        help="Comma-separated state slugs to compile (default: all 50)",
    )
    parser.add_argument(
        "--jobs", "-j",
        type=int,
        default=None,
        help=_help_workers,
    )
    parser.add_argument(
        "--passes",
        type=int,
        default=None,
        help=_help_passes,
    )
    return parser.parse_args()


# ============================================================================
# MAIN
# ============================================================================

def main() -> None:
    args = parse_args()

    workspace = (args.workspace or find_workspace()).resolve()
    print(f"Workspace: {workspace}")

    config = load_config(workspace)
    all_state_slugs = config.all_state_slugs

    # ── Interactive prompts when arguments are omitted ──────────────────
    book_type = args.book_type
    if book_type is None:
        book_type = prompt_book_type(
            list(BOOK_TYPES.keys()),
            allow_all=True,
            allow_all_previews=True,
        )

    if book_type == "all":
        bt_keys = NON_PREVIEW_KEYS
    elif book_type == "all_previews":
        bt_keys = PREVIEW_KEYS
    else:
        bt_keys = [book_type]

    if args.states:
        requested = [s.strip().lower() for s in args.states.split(",")]
        unknown = [s for s in requested if s not in all_state_slugs]
        if unknown:
            print(f"ERROR: Unknown state(s): {', '.join(unknown)}")
            print(f"  Valid slugs: {', '.join(sorted(all_state_slugs))}")
            sys.exit(1)
        state_slugs = requested
    else:
        states_input = prompt_states(all_state_slugs)
        if states_input is None:
            state_slugs = all_state_slugs
        else:
            state_slugs = [s.strip().lower() for s in states_input.split(",")]

    # -- Discover ALL .tex files across every requested book type --
    all_tex_paths: List[Path] = []
    for bt_key in bt_keys:
        found = 0
        for slug in state_slugs:
            p = workspace / "state_books" / slug / tex_filename(bt_key, slug)
            if p.exists():
                all_tex_paths.append(p)
                found += 1
        if found == 0:
            print(f"  Warning: No .tex files for {bt_key}")

    if not all_tex_paths:
        print("ERROR: No .tex files found. Run generate scripts first.")
        sys.exit(1)

    max_workers = args.jobs or MAX_WORKERS
    passes = args.passes or XELATEX_PASSES

    # -- Compile everything in ONE concurrent batch --
    print(f"\nCompiling {len(all_tex_paths)} books across {len(bt_keys)} type(s)")
    print(f"   Workers: {max_workers}  |  Passes: {passes}  |  States: {len(state_slugs)}")
    print()

    t0 = time.monotonic()
    succeeded, failed = compile_books_concurrent(
        all_tex_paths, workspace, max_workers, passes,
    )
    elapsed = time.monotonic() - t0

    sep = "=" * 60
    print(f"\n{sep}")
    print(f"Succeeded: {len(succeeded)}  |  Failed: {len(failed)}  |  Time: {elapsed:.1f}s")

    if failed:
        print("\nFailed compilations:")
        for msg in failed:
            print(f"   {msg[:200]}")
        sys.exit(1)


if __name__ == "__main__":
    main()
